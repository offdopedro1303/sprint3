<br>
Reduza o Consumo de Energia:
<br>
Desligue luzes, aparelhos eletrônicos e ar-condicionado ao sair do quarto.
Utilize cortinas para aproveitar a luz natural durante o dia.
<br>
<br>
Economize Água:
<br>
Tome banhos rápidos.
<br>
Reutilize toalhas e lençóis quando possível.
<br>
Relate vazamentos ou problemas com encanamentos.
<br>
<br>
Minimize o Desperdício de Plástico:
<br>
Leve uma garrafa de água reutilizável.
<br>
Evite produtos descartáveis, como talheres e copos.
<br>
Recuse sacolas plásticas e traga sua própria bolsa reutilizável.
<br>
<br>
Utilize Transporte Sustentável:
<br>
Opte por meios de transporte mais ecológicos, como bicicletas ou transporte público.
Se possível, escolha voos diretos para reduzir as emissões de carbono.</p>
